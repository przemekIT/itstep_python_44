# zad 1
def znajdz_sume(lista):
    """Funkcja znajduje sume z listy liczb całkowitych.
    :param lista: Lista liczb całkowitych
    :return: Suma z listy
    """
    return sum(lista)

# Przykład użycia
lista_liczb = [3,7,1,9,2] 
suma = znajdz_sume(lista_liczb)

print(f"Suma z listy: {suma}")

# zad 2
def znajdz_maksimum(lista):
    """Funckja znajduje maksimum na liście liczb całkowitych.
    :param lista: Lista liczb całkowitych
    :return: Maksymalna wartość na liście
    """
    return max(lista)

# Przykład użycia
lista_liczb = [3,7,1,9,2] 
maksimum = znajdz_maksimum(lista_liczb)

print(f"Maksimum na liście: {maksimum}")

